from time import sleep
from time import time
from os import system
symb = ''
symblist = list()
while True:
    symb = '(/)'
    symblist.append(symb)
    print(symblist)
    symblist.clear()
    system()
    sleep(0.5)
    symb = '(-)'
    symblist.append(symb)
    print(symblist)
    symblist.clear()
    sleep(0.5)
    symb = '(\)'
    symblist.append(symb)
    print(symblist)
    symblist.clear()
    sleep(0.5)
    symb = '(|)'
    symblist.append(symb)
    print(symblist)
    symblist.clear()
    sleep(0.5)
